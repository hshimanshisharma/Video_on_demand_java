    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String category=(String)cb.getSelectedItem();
        String movie = mname.getText();
        String direct = director.getText();
        String starcast = cast.getText();
        String uid=id.getText();
        String trid=tid.getText();
        
        if(category.equals("Select Category")||movie.equals("")||direct.equals("")||starcast.equals(" ") ||uid.equals("")||tid.equals("")||ph1==null)
        {
            JOptionPane.showMessageDialog(rootPane, "All Fields are mandatory");
        }
        else
        {
            String ans = myClient.addmovie(category,movie,direct,starcast,uid,trid,ph1);
            if(ans.equals("success"))
            {
                JOptionPane.showMessageDialog(rootPane, "Added");
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Failed");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

