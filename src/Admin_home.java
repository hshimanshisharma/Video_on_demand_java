     void loadcategories()
    {
        String ans = myClient.fetchcategories();
        
        StringTokenizer st = new StringTokenizer(ans,";;");
        while(st.hasMoreTokens())
        {
            String name = st.nextToken();
            cb.addItem(name);
        }
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = catname.getText();
        if(name.equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "All feilds are mandatory");
        }
        String ans = myClient.addcategory(name,ph);
        if(ans.equals("success"))
        {
            JOptionPane.showMessageDialog(rootPane, "Category Added");
        }
        else if(ans.equals("exist"))
        {
            JOptionPane.showMessageDialog(rootPane, "Category already exists");
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Failed");
        }
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

